package com.a2z.enums;

/**
 * Represents the available user roles in the system.
 *
 * @author Chetanand Meher
 */
public enum USER_ROLE {
    /**
     * Administrator role with full system access and management capabilities.
     */
    ADMIN,

    /**
     * Seller role for users who can list and manage products for sale.
     */
    SELLER,

    /**
     * Customer role for users who can browse and purchase products.
     */
    CUSTOMER
}
