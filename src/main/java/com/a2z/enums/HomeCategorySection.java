package com.a2z.enums;

/**
 * Represents the different category sections displayed on the home page.
 * This enum defines the layout structure and organization of product categories.
 *
 * @author Chetanand Meher
 */
public enum HomeCategorySection {
    /**
     * Section displaying electronics-related product categories.
     * Contains categories like phones, laptops, accessories etc.
     */
    ELECTRIC_CATEGORIES,

    /**
     * Section showing products in a grid layout format.
     * Displays multiple product categories in a structured grid view.
     */
    GRID,

    /**
     * Main shopping categories section.
     * Lists all available product categories for browsing.
     */
    SHOP_BY_CATEGORIES,

    /**
     * Section highlighting current deals and offers.
     * Shows promotional offers, discounts and special deals.
     */
    DEALS
}
